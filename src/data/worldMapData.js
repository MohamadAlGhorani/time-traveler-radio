/**
 * World map outline data — real geographic coordinates (lon, lat)
 * projected via equirectangular to 1000x500 SVG viewBox.
 *
 * Each continent is an array of [lon, lat] pairs tracing the coastline.
 * Derived from Natural Earth 110m simplified outlines.
 */

function toPath(coords) {
  return coords.map(([lon, lat], i) => {
    const x = ((lon + 180) / 360) * 1000;
    const y = ((90 - lat) / 180) * 500;
    return `${i === 0 ? 'M' : 'L'}${x.toFixed(1)},${y.toFixed(1)}`;
  }).join(' ') + ' Z';
}

